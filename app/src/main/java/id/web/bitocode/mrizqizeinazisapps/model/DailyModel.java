package id.web.bitocode.mrizqizeinazisapps.model;

public class DailyModel
{
  private int Id;
  private String NamaActivity;
  private String WaktuActivity;
  
  public int getId()
  {
    return Id;
  }
  
  public void setId(int id)
  {
    Id = id;
  }
  
  public String getNamaActivity()
  {
    return NamaActivity;
  }
  
  public void setNamaActivity(String namaActivity)
  {
    NamaActivity = namaActivity;
  }
  
  public String getWaktuActivity()
  {
    return WaktuActivity;
  }
  
  public void setWaktuActivity(String waktuActivity)
  {
    WaktuActivity = waktuActivity;
  }
}
